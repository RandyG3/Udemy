"""
PEMDAS: Order of operations
Parentheses, 
 Exponents, 
  Multiplication, 
    Division, 
     Addition, 
      Subraction
"""      

print(3 + 4)
print(3.5 + 6.3)
print(10 - 5)
print(5 * 3)

print("Ba" + "con") # Std concatenation
print("lolo" * 32) # repeats string 32 times

print(10 + 3.8) # converts to the more specific/precise (Float)

print(5 ** 3) # raise 5 to the 3rd
print(4 ** 4) # raise 5 to the 4th

print(3 + 5 * 3) # Multiplication, then addition
print((3 + 5) * 3)