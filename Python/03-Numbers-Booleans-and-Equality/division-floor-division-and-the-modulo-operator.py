print(15 / 3) # results will be in float regardless
print(14 / 3)

# Floor division truncates the decimal portion (remainder) and is an integer
# no rounding is performed
print( 14 // 3) 

print(-14 // 3) # round down from 0 when result is negative

# Modulo - returns the remainder
print(14 % 3) 
print(15 % 3) # 0 is returned if evenly divisible
print(16 % 3)

