for number in range(5): # Upto but not including
    print(number)
print()

for number in range(0, 5):  # Start, end
    print(number)
print()

for number in range(3, 9):  # Start, end
    print(number)
print()

for number in range(10, 101, 10):  # Start, end, step interval
    print(number)
print()

for number in range(99, -1, -11):  # Start, end, step interval
    print(number)
print()